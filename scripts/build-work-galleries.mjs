// Consolidates portfolio-images/<project> into public/work-images/<project>,
// keeping the largest variant of each unique image, and emits a manifest the
// work pages consume. Run with: node scripts/build-work-galleries.mjs
import {
  readdirSync,
  statSync,
  mkdirSync,
  copyFileSync,
  writeFileSync,
} from "node:fs";
import { join } from "node:path";

const SRC_ROOT = "portfolio-images";
const OUT_ROOT = join("public", "work-images");

// Project order + which image is the wide hero/banner for each page.
const PROJECTS = ["gemini", "pvz", "outriders", "ragtag", "vhs", "misc"];
const BANNERS = {
  gemini: "Clean_GeminiImage-1024x377.jpeg",
  pvz: "Clean_MainImage_PvZ-1-1024x428.png",
  outriders: "Project-Banner-2048x770.png",
  ragtag: "bX9X6ssJgSseK26JSTpy1f-2048x964.webp",
  vhs: "VHSBanner-3.png",
  misc: "slice1-scaled.png",
};
// Folder name -> the route slug used in src/lib/projects.ts.
const SLUGS = {
  gemini: "project-gemini",
  pvz: "plants-vs-zombies",
  outriders: "outriders",
  ragtag: "ragtag",
  vhs: "vhs",
  misc: "misc",
};

const IMG_EXT = /\.(png|jpe?g|gif|webp|avif)$/i;

// Strip WordPress "-1024x768" / "-scaled" suffixes to group resolution variants.
function baseKey(name) {
  return name
    .replace(/-\d+x\d+(\.[a-z0-9]+)$/i, "$1")
    .replace(/-scaled(\.[a-z0-9]+)$/i, "$1")
    .toLowerCase();
}

const manifest = {};

for (const proj of PROJECTS) {
  const srcDir = join(SRC_ROOT, proj);
  let files;
  try {
    files = readdirSync(srcDir).filter((f) => IMG_EXT.test(f));
  } catch {
    console.warn(`skip ${proj}: no folder`);
    continue;
  }

  // Group by base key, pick the largest file (best quality / resolution).
  const groups = new Map();
  for (const f of files) {
    if (/_dup/.test(f)) continue;
    const key = baseKey(f);
    const size = statSync(join(srcDir, f)).size;
    const cur = groups.get(key);
    if (!cur || size > cur.size) groups.set(key, { file: f, size });
  }

  const outDir = join(OUT_ROOT, proj);
  mkdirSync(outDir, { recursive: true });

  const chosen = [...groups.values()].map((g) => g.file);
  for (const f of chosen) copyFileSync(join(srcDir, f), join(outDir, f));

  manifest[proj] = chosen;
  console.log(`${proj}: ${files.length} files -> ${chosen.length} unique`);
}

writeFileSync("scripts/work-manifest.json", JSON.stringify(manifest, null, 2));
console.log("\nWrote scripts/work-manifest.json");

// Emit a typed galleries file the work pages import.
const lines = [
  "// AUTO-GENERATED by scripts/build-work-galleries.mjs — do not edit by hand.",
  "// Image lists mirror each original mattpearson.net project page.",
  "",
  "export type Gallery = { banner: string; images: string[] };",
  "",
  "export const galleries: Record<string, Gallery> = {",
];
for (const proj of PROJECTS) {
  const all = manifest[proj] || [];
  const banner = BANNERS[proj];
  const images = all.filter((f) => f !== banner);
  const base = `/work-images/${proj}/`;
  lines.push(`  "${SLUGS[proj]}": {`);
  lines.push(`    banner: "${base}${banner}",`);
  lines.push("    images: [");
  for (const f of images) lines.push(`      "${base}${f}",`);
  lines.push("    ],");
  lines.push("  },");
}
lines.push("};");
lines.push("");
writeFileSync("src/lib/workGalleries.ts", lines.join("\n"));
console.log("Wrote src/lib/workGalleries.ts");
